#!/usr/bin/env node
// Regenerate src/data/blog-posts-manifest.ts from every JSON in
// src/data/generated-blog-posts/.
//
// Revive OS's publish-blog-to-site drops a <slug>.json into that directory and commits it,
// but it does NOT hand-edit the manifest — so before this script existed, a published post
// was committed, deployed, and then 404'd forever because nothing imported it. (Liberty had
// 8 JSON files and 7 imports, 2026-07-22.)
//
// Static `import` statements are kept deliberately: the old hand-written manifest used them
// so every JSON is bundled into each consuming serverless function, and a runtime
// fs.readdir would break that. This script preserves the property and removes the hand-editing.
//
// Runs automatically via the `prebuild` npm lifecycle hook, so Vercel picks it up with no
// change to the build command.

import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const root = join(dirname(fileURLToPath(import.meta.url)), '..');
const postsDir = join(root, 'src/data/generated-blog-posts');
const outFile = join(root, 'src/data/blog-posts-manifest.ts');

const files = readdirSync(postsDir)
  .filter((f) => f.endsWith('.json'))
  .sort();

/** Turn a slug into a unique, valid TS identifier. Index suffix guarantees no collisions. */
const identFor = (file, i) => {
  const camel = file
    .replace(/\.json$/, '')
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((w, n) => (n === 0 ? w.toLowerCase() : w[0].toUpperCase() + w.slice(1).toLowerCase()))
    .join('')
    .slice(0, 40);
  return `post${i}_${camel || 'untitled'}`;
};

const entries = files.map((file, i) => ({ file, ident: identFor(file, i) }));

// Fail loudly rather than silently shipping a post with no slug — it would be
// unreachable and would poison the BLOG_POSTS lookup with an "undefined" key.
for (const { file } of entries) {
  const data = JSON.parse(readFileSync(join(postsDir, file), 'utf8'));
  if (!data.slug) throw new Error(`generate-blog-manifest: ${file} has no "slug" field`);
}

const out = `// AUTO-GENERATED by scripts/generate-blog-manifest.mjs — do not edit by hand.
// Runs on \`npm run prebuild\` (and therefore on every Vercel build), enumerating
// src/data/generated-blog-posts/. Add a post by committing its JSON; the import
// lines below are rewritten for you.
//
// Static imports are intentional: they guarantee every JSON is bundled into each
// consuming serverless function.

${entries.map(({ file, ident }) => `import ${ident} from './generated-blog-posts/${file}';`).join('\n')}

export interface BlogPost {
  slug: string;
  title: string;
  excerpt: string;
  metaDescription?: string;
  category: string;
  readTime: string;
  date: string;
  isoDate: string;
  author: string;
  tags: string[];
  heroImage?: string;
  heroImageAlt?: string;
  contentHtml?: string;
  contentBlocks?: string[];
}

const ALL: BlogPost[] = [
${entries.map(({ ident }) => `  ${ident} as BlogPost,`).join('\n')}
];

export const BLOG_POSTS: Record<string, BlogPost> = Object.fromEntries(
  ALL.map((p) => [p.slug, p]),
);

export function getBlogPost(slug: string): BlogPost | null {
  return BLOG_POSTS[slug] ?? null;
}

export function listBlogPosts(): BlogPost[] {
  return ALL;
}
`;

writeFileSync(outFile, out);
console.log(`generate-blog-manifest: wrote ${entries.length} posts to src/data/blog-posts-manifest.ts`);
